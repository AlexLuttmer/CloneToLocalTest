// 'And' logical operator (&&)

let myAge = 18

if (myAge >= 18 && myAge <=40) {
    console.log("You can come in!")
} else {
    console.log("Sorry, but GTFO!")
}

// 'Or' logical operator (||)

let broughtDrinks = true;

if (myAge >= 18 && myAge <=40 || broughtDrinks) {
    console.log("You can come in!")
} else {
    console.log("Sorry, but GTFO!")
}

